In order to add the database tables to your own SQLiteStudio local workspace, follow the steps below:

1. Create a new database and connect to it.

2. Copy and paste everything from "TablesAug2018.sql" onto your SQLite editor.

3. Highlight everything (CTRL+A) then run the code (F5)

4. If running gives an error (i.e. your database isn't populated with tables),
then try to fix it somehow or refer to the next step.

5. If fixing it doesn't work, then contact me and I'll give you a copy of the db file.

6. Whether you made your own database or used the copy I sent you:
if you're having security issues with writing on the database, then place the db file in "C:\Users\<YourUserName>".


